                currentMode.Info_Browse_Mode = "edit";
				Output.WriteLine("    <li><a href=\"" + SobekCM_Library_Settings.Base_SobekCM_Location_Relative + currentMode.Redirect_URL() + "\">" + online_edits + "</a></li>");
