                currentMode.Info_Browse_Mode = "submit";
				Output.WriteLine("    <li><a href=\"" + SobekCM_Library_Settings.Base_SobekCM_Location_Relative + currentMode.Redirect_URL() + "\">" + online_submits + "</a></li>");
