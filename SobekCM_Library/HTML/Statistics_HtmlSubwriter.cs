                if ((UI_ApplicationCache_Gateway.Settings.Statistics_Caching_Enabled) && (File.Exists(possible_source_file)))
                {
                    FileInfo sourceFileInfo = new FileInfo(possible_source_file);
                    TimeSpan fileAge = DateTime.Now.Subtract(sourceFileInfo.LastWriteTime);
                    if (fileAge.TotalMinutes < 120)
                    {
                        Tracer.Add_Trace("Statistics_HtmlSubwriter.add_item_count_html", "Pulling item count from <a href=\"" + possible_source_url + "\">recent cached data file</a> in temporary folder");
                        itemCount = new DataTable();
                        itemCount.ReadXml(possible_source_file);
                    }
                }
            }
            catch
            {
                Tracer.Add_Trace("Statistics_HtmlSubwriter.add_item_count_html", "Error reading the cached file", Custom_Trace_Type_Enum.Error);
            }

            // If no data pulled get it from the database
            if (itemCount == null)
            {
                // Pull from database 
                if (IncludesFytd)
                {
                    DateTime fiscalStart = new DateTime(DateTime.Now.Year, 7, 1);
                    if (DateTime.Compare(DateTime.Now, fiscalStart) < 0)
                    {
                        fiscalStart = new DateTime(DateTime.Now.Year - 1, 7, 1);
                    }
                    itemCount = SobekCM_Database.Get_Item_Aggregation_Count( fiscalStart, Tracer);
                }
                else
                {
                    itemCount = SobekCM_Database.Get_Item_Aggregation_Count(Tracer);
                }

                // Save as XML in the temporary folder
                if ((itemCount != null) && (UI_ApplicationCache_Gateway.Settings.Statistics_Caching_Enabled))
                {
                    itemCount.WriteXml(possible_source_file, XmlWriteMode.WriteSchema);
                }
            }

            // If there was no count, or no rows returned, say something
            if ((itemCount == null) || (itemCount.Rows.Count == 0))
            {
                Output.WriteLine("<b>NO ITEM COUNT AVAILABLE</b>");
                return;
            }

            // Declare these vairables, although we may not use them
            int ytd_titles = 0;
            int ytd_items = 0;
            int ytd_pages = 0;

            // Add the text first
            DataRow[] total_row = itemCount.Select("code1='ZZZ'");
            if (total_row.Length > 0)
            {
                int titles = Convert.ToInt32(total_row[0][6]);
                int items = Convert.ToInt32(total_row[0][7]);
                int pages = Convert.ToInt32(total_row[0][8]);

                int million_pages = (pages / 1000000);
                string page_string = million_pages.ToString();
                switch( million_pages )
                {
                    case 1:
                        page_string = "one";
                        break;

                    case 2:
                        page_string = "two";
                        break;

                    case 3:
                        page_string = "three";
                        break;

                    case 4:
                        page_string = "four";
                        break;

                    case 5:
                        page_string = "five";
                        break;

                    case 6:
                        page_string = "six";
                        break;

                    case 7:
                        page_string = "seven";
                        break;

                    case 8:
                        page_string = "eight";
                        break;

                    case 9:
                        page_string = "nine";
                        break;

                    case 10:
                        page_string = "ten";
                        break;
                }

				Output.WriteLine("<div class=\"sbkShw_MainText\">");
                Output.WriteLine("<p>The " + RequestSpecificValues.Current_Mode.SobekCM_Instance_Name + " ( " + RequestSpecificValues.Current_Mode.SobekCM_Instance_Abbreviation + " ) is comprised of collections, organized into collection groups and subdivided into subcollections.  Currently, there are " + items + " total items in " + RequestSpecificValues.Current_Mode.SobekCM_Instance_Abbreviation + ", which make up " + titles + " different titles.   In all, there are over " + page_string + " million pages in " + RequestSpecificValues.Current_Mode.SobekCM_Instance_Abbreviation + ".</p>");

                if (IncludesFytd)
                {
                    ytd_titles = titles - Convert.ToInt32(total_row[0][9]);
