            if (codeManager.isValidCode(Collection))
            {
                itemsListSet = SobekCM_Database.Statistics_Aggregation_Titles(Collection, Tracer);
            }
            else
            {
                Collection = codeManager.All_Aggregations[0].Code;
                itemsListSet = SobekCM_Database.Statistics_Aggregation_Titles(Collection, Tracer);
